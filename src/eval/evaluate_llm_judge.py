import argparse
import json
import os
import time
from typing import Any, Dict, List

from dotenv import load_dotenv
from huggingface_hub import InferenceClient
from tqdm import tqdm


def classify_completion(
    client: InferenceClient, model_id: str, prompt: str, completion: str
) -> str:
    """Uses Hugging Face Serverless Inference API to classify the familiarity of the completion."""
    eval_prompt = f"""You are an evaluator assessing a language model's familiarity with the Harry Potter book series.
You will be given a prompt (derived from the books) and a completion generated by the model.
Your task is to classify the completion based on RECALLED knowledge, not just echoing the prompt.

CRITICAL RULE: If the completion only contains names or terms that were ALREADY present in the prompt, it does NOT count as "Explicit" knowledge.

Categories:
1. "Explicit": The model introduces NEW, unique Harry Potter details (names, spells, events) that were NOT in the prompt.
2. "Thematic": The model uses general fantasy terms but no new unique HP details.
3. "Accidental": The model uses common words correctly but shows no HP context.
4. "None": The model is repetitive, degenerated, or completely unrelated.

Prompt:
```
{prompt}
```

Completion:
```
{completion}
```

Classification (Reply with EXACTLY ONE word: "Explicit", "Thematic", "Accidental", or "None"):"""

    try:
        response = client.chat_completion(
            model=model_id,
            messages=[
                {
                    "role": "system",
                    "content": "You are a precise evaluator focus on identifying knowledge recall vs. prompt echoing.",
                },
                {"role": "user", "content": eval_prompt},
            ],
            max_tokens=10,
            temperature=0.01,
        )

        text = (
            response.choices[0]
            .message.content.strip()
            .replace('"', "")
            .replace("'", "")
        )

        # Search for any valid label in the text
        text_clean = (
            text.lower()
            .replace(".", "")
            .replace(",", "")
            .replace("!", "")
            .replace(":", "")
        )
        tokens = text_clean.split()

        for label in ["explicit", "thematic", "accidental", "none"]:
            if label in tokens:
                return label.capitalize()

        print(f"Format Error. Raw response: '{text}'")
        return "Error_Format"

    except Exception as e:
        err_str = str(e).lower()
        if "402" in err_str:
            return "Error_Paid"
        if "429" in err_str or "rate" in err_str:
            print(f"Rate limited or quota issue. Waiting 20s...")
            time.sleep(20)
            return classify_completion(client, model_id, prompt, completion)
        if "503" in err_str or "loading" in err_str:
            print("Model is loading... waiting 20s")
            time.sleep(20)
            return classify_completion(client, model_id, prompt, completion)
        print(f"Error calling API: {e}")
        return "Error"


def main():
    parser = argparse.ArgumentParser(
        description="LLM-as-a-Judge Evaluation for HP Unlearning (HF Serverless)"
    )
    parser.add_argument(
        "--input",
        type=str,
        default="results/unified_eval_results.json",
        help="Path to evaluation results JSON",
    )
    parser.add_argument(
        "--output",
        type=str,
        default="results/unified_eval_results_judged.json",
        help="Path to save judged results",
    )
    parser.add_argument(
        "--limit", type=int, default=100, help="Max number of completions to judge"
    )
    # Using a small model highly likely to be on the free serverless tier
    parser.add_argument(
        "--model", type=str, default="HuggingFaceH4/zephyr-7b-beta", help="HF model ID"
    )
    args = parser.parse_args()

    load_dotenv()
    api_key = os.environ.get("HF_TOKEN")
    if not api_key:
        print(
            "Please set HF_TOKEN environment variable in /home/jay-18/inlp/INLP_PROJECT/.env"
        )
        return

    client = InferenceClient(token=api_key)

    print(f"Loading completions from {args.input}...")
    with open(args.input, "r") as f:
        data = json.load(f)

    side_by_side = data.get("side_by_side", [])
    if not side_by_side:
        print("No side-by-side completions found.")
        return

    eval_limit = min(args.limit, len(side_by_side))
    subset = side_by_side[:eval_limit]

    print(f"Evaluating {eval_limit} pairs with {args.model} via HF Serverless...")

    baseline_stats = {
        "Explicit": 0,
        "Thematic": 0,
        "Accidental": 0,
        "None": 0,
        "Error": 0,
        "Error_Format": 0,
    }
    ablated_stats = {
        "Explicit": 0,
        "Thematic": 0,
        "Accidental": 0,
        "None": 0,
        "Error": 0,
        "Error_Format": 0,
    }

    pbar = tqdm(subset, desc="Judging Completions")
    for item in pbar:
        prompt = item["prompt"]
        b_comp = item["baseline"]
        a_comp = item["ablated"]

        # Baseline check
        b_class = classify_completion(client, args.model, prompt, b_comp)

        # Ablated check
        a_class = classify_completion(client, args.model, prompt, a_comp)

        item["baseline_llm_judge"] = b_class
        item["ablated_llm_judge"] = a_class

        if b_class in baseline_stats:
            baseline_stats[b_class] += 1
        else:
            baseline_stats["Error"] += 1

        if a_class in ablated_stats:
            ablated_stats[a_class] += 1
        else:
            ablated_stats["Error"] += 1

        pbar.set_postfix({"b": b_class, "a": a_class})

    if "llm_judge" not in data["metrics"]["baseline"]:
        data["metrics"]["baseline"]["llm_judge"] = {}
        data["metrics"]["ablated"]["llm_judge"] = {}

    data["metrics"]["baseline"]["llm_judge"] = baseline_stats
    data["metrics"]["ablated"]["llm_judge"] = ablated_stats

    # Save back
    with open(args.output, "w") as f:
        json.dump(data, f, indent=2)

    print("\n" + "=" * 50)
    print("LLM JUDGE EVALUATION SUMMARY")
    print("=" * 50)
    print(f"Evaluated: {eval_limit} pairs")
    print("\nBaseline Classifications:")
    for k, v in baseline_stats.items():
        if v > 0:
            print(f"  {k}: {v} ({v / eval_limit * 100:.1f}%)")

    print("\nAblated Classifications:")
    for k, v in ablated_stats.items():
        if v > 0:
            print(f"  {k}: {v} ({v / eval_limit * 100:.1f}%)")
    print("=" * 50)
    print(f"Results saved to {args.output}")


if __name__ == "__main__":
    main()
